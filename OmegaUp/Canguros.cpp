#include <iostream>
#include <queue>
using namespace std;

void function()
{
    int C, N;
    char S;
    scanf("%d%d", &C, &N);
    while (N--)
    {
        scanf("%c", &S);
        if (S == 'N')
        {
            char M;
            scanf("%c",&M);
        }
    }
}

main(int argc, char const *argv[])
{
    function();
    return 0;
}
